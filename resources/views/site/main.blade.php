    function resize() {
        var h = $(".carousel-container img").height();
        var doc_w = $(document).width();
        <!-- gallery image height temp patch-->
        var hgal = $(".gallery .wrapper .col-sm-3").height();
        <!-- ** -->
        if (doc_w > 650) {
            var mh = $(".mainmenu-bar").height();
            $(".top-block").height(h - (mh + 5));
            <!-- gallery image height temp patch-->
            $(".gallery .wrapper .col-sm-6 img").height(hgal);
            <!-- ** -->
        } else {
            var mh = $(".mobile").height();
            $(".top-block").height(h - (mh + 7.5));
        };
    };
